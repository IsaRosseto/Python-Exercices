#     • Pig Latin é uma língua construída pela transformação de palavras da
#     língua inglesa.
#     • Embora as origens da língua sejam desconhecidas, ela é mencionada
#     em pelo menos dois documentos do século XIX, sugerindo que ela
#     existe há mais de 100 anos.

#     Regras usadas para traduzir do inglês para o Pig Latin:
# • 1-) Se a palavra começar com consoante(incluindo y), todas as letras
#     no início da palavra, até a primeira vogal(excluindo y), serão removidas
#     e adicionadas ao final da palavra, seguidas de ay.
#     • Por exemplo:
#     – computer se torna omputercay e think se torna inkthay.

#     Regras usadas para traduzir do inglês para o Pig Latin:
#     • 2-) Se a palavra começar com uma vogal(não incluindo y), então way é
#     adicionado ao final da palavra.
#     • Por exemplo:
#     – o algorithm se torna algorithmway e office se torna officeway.

